$(document).ready(function (e) {
    $('#transport').hover(function(){
            $('#sub').height(60+'px');
            $('#first,#first_2,#first_3,#first_4').css('display','block');
        },
        function(){
                $('#sub').hover(
                    function(){
                        $('#sub').height(60+'px');
                        $('#first,#first_2,#first_3,#first_4').css('display','block');
                    },
                    function(){
                        $('#sub').height(0+'px');
                        $('#first,#first_2,#first_3,#first_4').css('display','none');
                    }
                );
            $('#sub').height(0+'px');
            $('#first,#first_2,#first_3,#first_4').css('display','none');
        }
    );
});
